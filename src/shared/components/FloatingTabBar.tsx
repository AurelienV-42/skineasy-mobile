import { useTabTrigger } from 'expo-router/ui'
import { CalendarDays, Home, Sparkles } from 'lucide-react-native'
import { useEffect, useState } from 'react'
import { useTranslation } from 'react-i18next'
import { LayoutChangeEvent, Pressable, StyleSheet, Text, View } from 'react-native'
import Animated, { useAnimatedStyle, useSharedValue, withSpring } from 'react-native-reanimated'
import { useSafeAreaInsets } from 'react-native-safe-area-context'

import { GlassContainer } from '@shared/components/GlassContainer'
import { colors } from '@theme/colors'

export const SPRING_CONFIG = { damping: 60, stiffness: 300 }

type TabConfig = {
  name: string
  href: string
  labelKey: string
  icon: typeof Home
}

const TABS: TabConfig[] = [
  { name: 'index', href: '/', labelKey: 'dashboard.home', icon: Home },
  { name: 'calendar', href: '/calendar', labelKey: 'calendar.title', icon: CalendarDays },
  { name: 'routine', href: '/routine', labelKey: 'routine.title', icon: Sparkles },
]

const TAB_COUNT = TABS.length
const BUBBLE_PADDING = 8

type TabButtonProps = {
  tab: TabConfig
  onFocusChange: (index: number, isFocused: boolean) => void
  index: number
}

function TabButton({ tab, onFocusChange, index }: TabButtonProps): React.ReactElement {
  const { t } = useTranslation()
  const { trigger, triggerProps } = useTabTrigger({ name: tab.name, href: tab.href })
  const Icon = tab.icon
  const isFocused = trigger?.isFocused ?? false
  const color = isFocused ? colors.primary : colors.textMuted

  useEffect(() => {
    onFocusChange(index, isFocused)
  }, [isFocused, index, onFocusChange])

  return (
    <Pressable
      className="flex-1 items-center gap-1 py-1"
      onPress={triggerProps.onPress}
      onLongPress={triggerProps.onLongPress}
    >
      <Icon color={color} size={24} />
      <Text className="font-medium text-xs" style={{ color }}>
        {t(tab.labelKey)}
      </Text>
    </Pressable>
  )
}

type TabBarContentProps = {
  containerWidth: number
}

function TabBarContent({ containerWidth }: TabBarContentProps): React.ReactElement {
  const activeIndex = useSharedValue(0)
  const tabWidth = containerWidth / TAB_COUNT
  const bubbleWidth = tabWidth - BUBBLE_PADDING * 2

  const handleFocusChange = (index: number, isFocused: boolean): void => {
    if (isFocused) {
      activeIndex.value = withSpring(index, SPRING_CONFIG)
    }
  }

  const bubbleStyle = useAnimatedStyle(() => ({
    transform: [{ translateX: activeIndex.value * tabWidth + BUBBLE_PADDING }],
    width: bubbleWidth,
  }))

  return (
    <>
      <Animated.View style={[styles.bubble, bubbleStyle]} />
      {TABS.map((tab, index) => (
        <TabButton key={tab.name} tab={tab} index={index} onFocusChange={handleFocusChange} />
      ))}
    </>
  )
}

export function FloatingTabBar(): React.ReactElement {
  const insets = useSafeAreaInsets()
  const [containerWidth, setContainerWidth] = useState(0)

  const bottomPosition = Math.max(insets.bottom, 16)

  const handleLayout = (event: LayoutChangeEvent): void => {
    setContainerWidth(event.nativeEvent.layout.width)
  }

  return (
    <View className="absolute left-[15%] right-[15%]" style={{ bottom: bottomPosition }}>
      <GlassContainer style={styles.glassContainer} glassStyle="regular" onLayout={handleLayout}>
        {containerWidth > 0 && <TabBarContent containerWidth={containerWidth} />}
      </GlassContainer>
    </View>
  )
}

const styles = StyleSheet.create({
  glassContainer: {
    flexDirection: 'row',
    alignItems: 'center',
    paddingVertical: 8,
    borderRadius: 32,
  },
  bubble: {
    position: 'absolute',
    top: 6,
    bottom: 6,
    backgroundColor: `${colors.primary}20`,
    borderRadius: 24,
  },
})
